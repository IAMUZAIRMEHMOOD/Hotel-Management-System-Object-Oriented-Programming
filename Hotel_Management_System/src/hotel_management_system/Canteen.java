    canteen.setCost_extras(canteen.getExtras_cheese() * extrascheese + canteen.getExtras_souces() * extrassouces);
    canteen.setCost_barbq(canteen.getBarbq_chicken() * barbqchickentikka + canteen.getBarbq_green() * barbqgreentikka + canteen.getBarbq_malai() * barbqmalaitikka);
    canteen.setCost_chickenmania(canteen.getChickenmania_chickenstripes() * chickenmaniachickenstripes + canteen.getChickenmania_flamingwing() * chickenmaniaflamingwing +
    canteen.getChickenmania_crunchyfriedchicken()*chickenmaniacrunchyfriedchicken );
    canteen.setCost_deals(canteen.getDeals_crunchychickenburger_softdrink_chickenstripes() * dealcrunchychickenburger + canteen.getDeals_crunchybeefburger_softdrink_crunchyfriedchicken() * dealcrunchybeefburger);
    canteen.setTotal(canteen.getCost_burger()+canteen.getCost_barbq()+canteen.getCost_beverages()+canteen.getCost_chickenmania()+canteen.getCost_deals()+canteen.getCost_extras());
//Printing sum 
cost_burger.setText(String.valueOf(canteen.getCost_burger()));
cost_beverages.setText(String.valueOf(canteen.getCost_beverages()));
cost_extras.setText(String.valueOf(canteen.getCost_extras()));
