    canteen.setCost_burger(canteen.getBurger_crunchybeef() * burgercrunchybeef + canteen.getBurger_crunchychicken() * burgercrunchychicken + canteen.getBurger_grilled_hunterbeef() * burgerclassicgrilledandhunterbeef);
